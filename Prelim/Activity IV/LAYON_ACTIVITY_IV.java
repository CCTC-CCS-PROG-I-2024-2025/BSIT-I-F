class LAYON_ACTIVITY_IV {
   public static void main(String[] args) {
      int x = 12;
      int y = 4;
      int z = 6;

      int sum = x + y + z;
      int diff = x - z;
      int prod = x * y;
      int qout = x / y;

      System.out.println(sum);
      System.out.println(diff);
      System.out.println(prod);
      System.out.println(qout);
   }
}
